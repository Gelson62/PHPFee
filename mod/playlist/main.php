<u>Playlist</u>
<br />
Ein Klick auf Playlist Starten fordert euch auf, welche der vorhandenen Playlisten Ihr Starten wollt. Ein Klick auf Ausfuehren generiert eine Playlist ( Dauert weniger als eine Sekunde) und spielt diese Anschlie&szlig;end ab.
<br /><br />
Mit einem Klick auf Playlist Stopp k&ouml;nnt ihr die aktuell Laufende Playlist beenden um auf den Stream zu gehen. Beachtet bitte das die Playlist im gleichen Moment wenn die gepoppt wird, auch aus ist. Seit bitte dann schnell auf dem Stream, um unn&ouml;tiges Buffern zu vermeiden.
<br /><br />

<b>Wie starte ich die Playlist?<br />
- Playlist starten klicken -> Playlist w&auml;hlen -> auf STARTEN klicken<br /><br />
Wie stoppe ich die Playlist?<br />
- Playliste stoppen klicken -> Best&auml;tigen -> Die Playlist ist sofort aus, also <u>immer sofort</u> danach <u>connecten</u> !</b>

